% UF²C: User Friendly Functional Connectivity
% 
% UF²C is an open source software developed by Brunno M. de Campos at the 
% Neuroimaging Laboratory at Unicamp that aims to simplify and organize 
% functional connectivity studies in neuroimaging through a clean and 
% validated methodology, without sacrificing quality. UF²C has a full 
% processing pipeline: The user only needs to select the raw functional 
% and structural NIfTI files from the subjects. The graphical user interface
% makes the processing and analysis options accessible for neuroscientists, 
% with reasonable choices of default settings. UF²C allows the user to study
% functional connectivity both through a quantitative view that provides 
% detailed values of average connectivity, and through a spatial view that 
% provides statistical maps that can be directly used for further analyses.
% All results are carefully organized in distinct folders for each subject,
% and a common folder is generated with a log file reporting the quantitative
% results of all the analyzed subjects.
% 
% UF²C is open source software, distributed under a BSD-style License. UF²C requires a computer with:
% 
